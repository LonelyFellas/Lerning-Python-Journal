# int 
number = 1
print(type(number)) # <class 'int'>
# float
floatNum = 1.222
print(type(floatNum)) # <class 'float'>
# str
strs = "Hello World"
print(type(strs)) # <class, 'str'>
# list
a = [1, 2, 3]
print(type(a)) # <class, 'list>
# tuple
c = {1, 2, 3}
print(type(c)) # <class, 'tuple'>
# 'dict'
d = {"name": "Alice", 'age': 25}
print(type(d)) # <class, 'dict'>
# bool
e = True
print(type(e)) # <class, 'bool'>